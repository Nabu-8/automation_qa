#Напишіть цикл, який буде вимагати від користувача ввести слово, в якому є літера "h" (враховуються як великі
# так і маленькі). Цикл не повинен завершитися, якщо користувач ввів слово без букви "h".
while True:
    line = input('Введіть слово в якому є літера "h" або "H" ')
    if 'h' in line.lower():
        print('Молодець!')
        break
    else:
        print('В цьому слові немає "h" або "H".')